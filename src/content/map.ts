/**
 * src/content/map.ts
 * ------------------
 * The whole of Varath, as one continuous walkable map (RULE 3). Ironvale, the
 * walled city, sits at the CENTRE of the world; the six wilderness regions are
 * spread out in a ring around it and joined to its gates by roads:
 *
 *                      The Spine (north)
 *        Marrow Deeps (NE) ·  IRONVALE  · (the home Knuckle Hills wrap it)
 *        Greyoak (west)   ·   [centre]  ·  Redrun & Sea (east)
 *              Heartmoor (SW)  ·  Ashfen Flats (south)
 *
 * Each wilderness region keeps the exact internal layout it always had — it is
 * generated by the same tile rule, just translated to its new home (so every
 * resource/monster keeps its relative place). Ironvale is hand-built at the
 * centre with a market street, a civic yard and four gates. Roads are carved
 * last so the centre always connects out to every region.
 */

