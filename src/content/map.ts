/**
 * src/content/map.ts
 * ------------------
 * The whole of Varath, as one continuous walkable map (RULE 3). Ironvale, the
 * walled city, sits at the CENTRE of a wide world; the six wilderness regions
 * are spread far out in a ring around it, joined to its gates by long roads:
 *
 *                      The Spine (north)
 *        Marrow Deeps (NE) ·  IRONVALE  · (the home Knuckle Hills wrap it)
 *        Greyoak (west)   ·   [centre]  ·  Redrun & Sea (east)
 *              Heartmoor (SW)  ·  Ashfen Flats (south)
 *
 * Each wilderness region keeps the exact internal layout it always had — it is
 * generated by the same tile rule, just translated to its new home (so every
 * resource/monster keeps its relative place). Ironvale and its surrounding
 * Knuckle Hills + hamlets are the "central complex": hand-built, then shifted
 * as a block to the centre of the bigger canvas. Roads are carved last so the
 * centre always connects out to every region across the open country between.
 *
 * Doubling the world (the "spread apart" update): the canvas grew to ~160×164,
 * the central complex moved by CENTRAL_SHIFT, and the regions were pushed much
 * further out. `remap()` is the single transform that re-homes the legacy
 * spawn coordinates onto the new canvas (region spawns by their region's delta;
 * everything else by CENTRAL_SHIFT; the hidden bands drop by BAND_DY).
 */

