function decode(): WorldMap {
  const tiles: TileType[] = new Array(WIDTH * HEIGHT).fill("grass");
  const set = (x: number, y: number, t: TileType) => {
    if (x >= 0 && y >= 0 && x < WIDTH && y < HEIGHT) tiles[y * WIDTH + x] = t;
  };
  const carve = (x0: number, y0: number, x1: number, y1: number, t: TileType) => {
    for (let y = y0; y <= y1; y++) for (let x = x0; x <= x1; x++) set(x, y, t);
  };
  // Central-complex carves: authored in LEGACY coords, shifted by CENTRAL_SHIFT
  // so they ride with the re-centred city (mirrors remap() for spawns).
  const cc = (x0: number, y0: number, x1: number, y1: number, t: TileType) =>
    carve(x0 + CDX, y0 + CDY, x1 + CDX, y1 + CDY, t);

  // 1) The arena band starts as solid wall; rooms are carved later.
  for (let y = OVERWORLD_HEIGHT; y < HEIGHT; y++) {
    for (let x = 0; x < WIDTH; x++) set(x, y, "wall");
  }

  // 2) Paint each wilderness region at its new home (same internal layout).
  for (const r of REGIONS) {
    for (let cy = r.ny; cy < r.ny + r.h; cy++) {
      for (let cx = r.nx; cx < r.nx + r.w; cx++) {
        const ox = cx - r.nx + r.ogx;
        const oy = cy - r.ny + r.ogy;
        set(cx, cy, r.fn(ox, oy));
      }
    }
  }

  // 2b) Repair any floor pocket a clump walled off, so every cavern/ledge stays
  //     reachable (it only opens blockers, never strands a spawn).
  const inRect = (x: number, y: number, x0: number, y0: number, x1: number, y1: number): boolean =>
    x >= x0 && x <= x1 && y >= y0 && y <= y1;
  const repairConnectivity = (
    x0: number, y0: number, x1: number, y1: number, floorAt: (x: number, y: number) => TileType,
  ): void => {
    const walkableHere = (x: number, y: number): boolean =>
      inRect(x, y, x0, y0, x1, y1) && !BLOCKED.has(tiles[y * WIDTH + x]!);
    const seen = new Set<number>();
    const pockets: [number, number][][] = [];
    for (let y = y0; y <= y1; y++) for (let x = x0; x <= x1; x++) {
      const idx = y * WIDTH + x;
      if (!walkableHere(x, y) || seen.has(idx)) continue;
      const cells: [number, number][] = []; const q: [number, number][] = [[x, y]]; seen.add(idx);
      while (q.length) {
        const [cx, cy] = q.shift()!; cells.push([cx, cy]);
        for (const [dx, dy] of [[1, 0], [-1, 0], [0, 1], [0, -1]] as const) {
          const nx = cx + dx, ny = cy + dy, ni = ny * WIDTH + nx;
          if (walkableHere(nx, ny) && !seen.has(ni)) { seen.add(ni); q.push([nx, ny]); }
        }
      }
      pockets.push(cells);
    }
    if (pockets.length <= 1) return;
    pockets.sort((a, b) => b.length - a.length);
    const centre = (c: [number, number][]): [number, number] =>
      [Math.round(c.reduce((s, p) => s + p[0], 0) / c.length), Math.round(c.reduce((s, p) => s + p[1], 0) / c.length)];
    const [mx, my] = centre(pockets[0]!);
    for (let i = 1; i < pockets.length; i++) {
      let [cx, cy] = centre(pockets[i]!);
      let guard = 0;
      while ((cx !== mx || cy !== my) && guard++ < 200) {
        if (cx !== mx) cx += Math.sign(mx - cx);
        else if (cy !== my) cy += Math.sign(my - cy);
        if (inRect(cx, cy, x0, y0, x1, y1) && BLOCKED.has(tiles[cy * WIDTH + cx]!)) set(cx, cy, floorAt(cx, cy));
      }
    }
  };

  const marrow = REGIONS.find((r) => r.key === "marrow")!;
  const spine = REGIONS.find((r) => r.key === "spine")!;
  repairConnectivity(marrow.nx, marrow.ny, marrow.nx + marrow.w - 1, marrow.ny + marrow.h - 1, () => "cave");
  repairConnectivity(spine.nx, spine.ny, spine.nx + spine.w - 1, spine.ny + spine.h - 1, (x, y) => {
    const sx = (x - spine.nx + spine.ogx) - 28;
    return sx < 6 || (y - spine.ny + spine.ogy) < 5 ? "stone" : "snow";
  });

  // 3) The home Knuckle Hills wrap the city: a dirt grove + rock outcrop to the
  //    north, a lake to the south-west, open hill grass all around.
  cc(44 - 6, 38 - 14, 79 + 6, 38 - 1, "grass"); // hill apron north
  cc(50, 24, 70, 33, "dirt");   // the dirt grove (Forestry)
  cc(82, 28, 92, 38, "stone");  // the knucklestone outcrop (Mining), NE
  cc(34, 70, 43, 82, "water");  // the lake on the city's south-west flank
  cc(80, 38, 86, 67, "grass");  // east apron to the river road

  // 4) Build Ironvale at the centre (CITY/GATES are already shifted).
  //    Plaza floor: stone in the civic (west) half, dirt in the market (east).
  carve(CITY.x0, CITY.y0, CITY.x1, CITY.y1, "stone");
  cc(63, 39, 79 - 1, 67 - 1, "dirt"); // market ground (east)
  //    Wall ring.
  for (let x = CITY.x0; x <= CITY.x1; x++) { set(x, CITY.y0, "wall"); set(x, CITY.y1, "wall"); }
  for (let y = CITY.y0; y <= CITY.y1; y++) { set(CITY.x0, y, "wall"); set(CITY.x1, y, "wall"); }
  //    Main streets (a cross joining the four gates).
  cc(59, 38, 61, 67, "path"); // N–S high street
  cc(44, 51, 79, 53, "path"); // E–W high street
  //    Four gates (open the wall ring where the roads meet it).
  carve(GATES.north.x - 1, CITY.y0, GATES.north.x + 1, CITY.y0, "path");
  carve(GATES.south.x - 1, CITY.y1, GATES.south.x + 1, CITY.y1, "path");
  carve(CITY.x0, GATES.west.y - 1, CITY.x0, GATES.west.y + 1, "path");
  carve(CITY.x1, GATES.east.y - 1, CITY.x1, GATES.east.y + 1, "path");
  //    Outlying hamlets: a packed-dirt yard, then a vacant homestead lot beside.
  cc(81, 57, 90, 63, "dirt");  // Redmouth, on the Redrun's east bank
  cc(63, 72, 73, 79, "dirt");  // The Drover's Rest, on the south road
  cc(58, 13, 67, 19, "dirt");  // The Fold, in the upland Knuckle Hills
  cc(81, 64, 86, 67, "dirt");  // Redmouth homestead lot
  cc(74, 73, 79, 76, "dirt");  // Drover's Rest homestead lot
  cc(59, 8, 64, 11, "dirt");   // The Fold homestead lot
  //    The buildings: each footprint becomes blocking masonry (roofed by the renderer).
  for (const b of BUILDINGS) carve(b.x0, b.y0, b.x1, b.y1, "wall");
  //    A flagged town square around the crossroads, for the fountain + crowds.
  cc(58, 50, 62, 54, "stone");

  // 4b) Countryside features around the city (the bible's named Knuckle Hills
  //     landmarks) — shifted with the central complex.
  cc(8, 6, 14, 12, "dirt");     // Rook's Watch — a high firepit knoll
  cc(18, 8, 26, 16, "stone");   // The Knuckle — the bald stone fist
  cc(14, 18, 22, 24, "dirt");   // an ashwood grove
  cc(28, 29, 33, 34, "water");  // a hill tarn (a head-stream pool)
  cc(5, 30, 11, 36, "stone");   // The Coldvein Scar — a played-out cutting
  cc(2, 64, 8, 70, "dirt");     // The Charburner's Clearing

  // 4d) NEW POIs filling the wide open country the bigger map opened up. Each is
  //     a patch of real ground a player can find between the city and a region.
  carve(36, 40, 44, 47, "stone");    // The Wayfarers' Crossroads ruin (NW, city→greyoak/spine)
  carve(112, 46, 122, 56, "stone");  // The Old Quarry (NE, city→marrow)
  carve(110, 96, 122, 106, "grass"); // The East Commons — drovers' meadow (E, city→redrun)
  carve(96, 112, 108, 124, "dirt");  // The Gallowsfield camp (SE, city→ashfen)
  carve(36, 104, 48, 116, "bog");    // The Sunken Mile (SW, city→heartmoor)
  carve(30, 80, 42, 90, "dirt");     // The Wood-Moor Verge (W, city→greyoak)

  // 4c) Heath: give the remaining open grass a rolling, mixed texture instead of
  //     a flat green sheet (all walkable — grass / dirt / moss hill country).
  for (let y = 0; y < OVERWORLD_HEIGHT; y++) {
    for (let x = 0; x < WIDTH; x++) {
      if (tiles[y * WIDTH + x] !== "grass") continue;
      const n = noise(x, y);
      if (n > 0.86) set(x, y, "moss");
      else if (n > 0.66) set(x, y, "dirt");
      // else: stays grass
    }
  }

  // 5) Long roads from the gates out to each far region (3 wide, walkable). The
  //    regions sit far from the centre now, so these are real journeys.
  const greyoak = REGIONS.find((r) => r.key === "greyoak")!;
  const ashfen = REGIONS.find((r) => r.key === "ashfen")!;
  const heartmoor = REGIONS.find((r) => r.key === "heartmoor")!;
  const redrun = REGIONS.find((r) => r.key === "redrun")!;
  const road = (x0: number, y0: number, x1: number, y1: number) => carve(x0, y0, x1, y1, "path");
  const stoneRd = (x0: number, y0: number, x1: number, y1: number) => carve(x0, y0, x1, y1, "stone");
  // North gate → the Spine pass (its walkable stone pass is at its west edge).
  road(GATES.north.x - 1, spine.ny + 14, GATES.north.x + 1, CITY.y0 - 1); // up to the pass
  road(spine.nx + 2, spine.ny + 13, GATES.north.x + 1, spine.ny + 15);    // west along to the pass foot
  stoneRd(spine.nx, spine.ny + 5, spine.nx + 3, spine.ny + 16);           // into the Spine pass
  // West gate → Greyoak Wood (its Lodge road is at local x13–14 → canvas nx+13).
  road(greyoak.nx + 14, GATES.west.y - 1, CITY.x0 - 1, GATES.west.y + 1);
  // South gate → Ashfen Flats via a long moor road.
  road(GATES.south.x - 1, CITY.y1 + 1, GATES.south.x + 1, ashfen.ny);
  road(ashfen.nx + 8, ashfen.ny - 2, GATES.south.x + 1, ashfen.ny);
  // South-west spur → Heartmoor (its road is at local x13–14 → canvas nx+13).
  road(heartmoor.nx + 14, GATES.south.y, heartmoor.nx + 16, heartmoor.ny); // down the moor
  road(heartmoor.nx + 14, GATES.south.y, GATES.south.x, GATES.south.y + 2); // link to the south road
  // East gate → the Redrun & the river road.
  road(CITY.x1 + 1, GATES.east.y - 1, redrun.nx, GATES.east.y + 1);       // east across the commons
  road(redrun.nx - 1, GATES.east.y, redrun.nx + 1, redrun.ny);            // down to the riverbank
  // North-east spur → the Marrow Deeps cave mouth (an L: up the outcrop, then
  //    across to the cave). carve() only fills left→right / top→down, so keep
  //    each leg's corners ordered.
  stoneRd(CITY.x1, marrow.ny + 15, CITY.x1 + 2, GATES.east.y);            // up from the east road
  stoneRd(CITY.x1, marrow.ny + 15, marrow.nx, marrow.ny + 16);           // across to the outcrop
  carve(marrow.nx, marrow.ny + 12, marrow.nx + 3, marrow.ny + 16, "cave"); // into the cave mouth

  // 5b) Short lanes joining the outlying hamlets to the road network.
  cc(84, 54, 85, 57, "path");   // Redmouth lane up to the east road
  cc(61, 75, 64, 76, "path");   // The Drover's Rest, off the south road
  cc(58, 19, 60, 24, "path");   // The Fold, down to the grove

  // 6) Carve the four sealed boss arenas in the band below the overworld.
  for (const a of ARENAS) {
    for (let y = ARENA_TOP; y < ARENA_TOP + ARENA_H; y++) {
      for (let x = a.x; x < a.x + ARENA_W; x++) {
        const edge = x === a.x || x === a.x + ARENA_W - 1 || y === ARENA_TOP || y === ARENA_TOP + ARENA_H - 1;
        set(x, y, edge ? "wall" : a.floor);
      }
    }
  }

  // 7) Carve each player-home interior in the band below the arenas.
  for (const h of HOMES) {
    const plan = homeLayout(h.ox);
    for (const rm of plan.rooms) {
      for (let y = rm.y0; y <= rm.y1; y++) {
        for (let x = rm.x0; x <= rm.x1; x++) {
          const edge = x === rm.x0 || x === rm.x1 || y === rm.y0 || y === rm.y1;
          set(x, y, edge ? "wall" : "plank");
        }
      }
    }
    for (const d of plan.doorways) set(d.x, d.y, "plank"); // openings (incl. the sealed wing doorway)
  }

  return { name: "Varath", width: WIDTH, height: HEIGHT, tiles };
}

export const map: WorldMap = decode();

/** A bounded "instance" rectangle (inclusive tile bounds) for view isolation. */
export interface InstanceRect { x0: number; y0: number; x1: number; y1: number }

/**
 * The instance the given tile belongs to — a player home or a boss arena — or
 * null for the open overworld. The renderer uses this to show ONLY the current
 * instance (a sealed room), so you never see the neighbouring rooms or the
 * arena band around it.
 */
export function instanceRectAt(x: number, y: number): InstanceRect | null {
  if (y < OVERWORLD_HEIGHT) return null; // the overworld is never masked
  for (const h of HOMES) {
    if (x >= h.ox && x <= h.ox + HOUSE_W - 1 && y >= INTERIOR_TOP && y <= INTERIOR_TOP + 10) {
      return { x0: h.ox, y0: INTERIOR_TOP, x1: h.ox + HOUSE_W - 1, y1: INTERIOR_TOP + 10 };
    }
  }
  for (const a of ARENAS) {
    if (x >= a.x && x <= a.x + ARENA_W - 1 && y >= ARENA_TOP && y <= ARENA_TOP + ARENA_H - 1) {
      return { x0: a.x, y0: ARENA_TOP, x1: a.x + ARENA_W - 1, y1: ARENA_TOP + ARENA_H - 1 };
    }
  }
  return null;
}

/** Convenience: read the tile type at a coordinate (grass if out of bounds). */
export function tileAt(m: WorldMap, x: number, y: number): TileType {
  if (x < 0 || y < 0 || x >= m.width || y >= m.height) return "grass";
  return m.tiles[y * m.width + x]!;
}
