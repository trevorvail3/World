/** Roof style at a tile (so the renderer knows which walls are buildings). */
const roofCells = new Map<string, RoofStyle>();
const doorCells = new Set<string>();
for (const b of BUILDINGS) {
  for (let y = b.y0; y <= b.y1; y++) for (let x = b.x0; x <= b.x1; x++) roofCells.set(`${x},${y}`, b.roof);
  if (b.door) doorCells.add(`${b.door.x},${b.door.y}`);
}
export function cityRoof(x: number, y: number): RoofStyle | undefined { return roofCells.get(`${x},${y}`); }
export function cityDoor(x: number, y: number): boolean { return doorCells.has(`${x},${y}`); }

function decode(): WorldMap {
  const tiles: TileType[] = new Array(WIDTH * HEIGHT).fill("grass");
  const set = (x: number, y: number, t: TileType) => {
    if (x >= 0 && y >= 0 && x < WIDTH && y < HEIGHT) tiles[y * WIDTH + x] = t;
  };
  const carve = (x0: number, y0: number, x1: number, y1: number, t: TileType) => {
    for (let y = y0; y <= y1; y++) for (let x = x0; x <= x1; x++) set(x, y, t);
  };

  // 1) The arena band starts as solid wall; rooms are carved later.
  for (let y = OVERWORLD_HEIGHT; y < HEIGHT; y++) {
    for (let x = 0; x < WIDTH; x++) set(x, y, "wall");
  }

  // 2) Paint each wilderness region at its new home (same internal layout).
  for (const r of REGIONS) {
    for (let cy = r.ny; cy < r.ny + r.h; cy++) {
      for (let cx = r.nx; cx < r.nx + r.w; cx++) {
        const ox = cx - r.nx + r.ogx;
        const oy = cy - r.ny + r.ogy;
        set(cx, cy, r.fn(ox, oy));
      }
    }
  }

  // 3) The home Knuckle Hills wrap the city: a dirt grove + rock outcrop to the
  //    north, a lake to the south-west, open hill grass all around.
  carve(CITY.x0 - 6, CITY.y0 - 14, CITY.x1 + 6, CITY.y0 - 1, "grass"); // hill apron north
  carve(50, 24, 70, 33, "dirt");   // the dirt grove (Forestry)
  carve(82, 28, 92, 38, "stone");  // the knucklestone outcrop (Mining), NE
  carve(34, 70, 43, 82, "water");  // the lake on the city's south-west flank
  carve(CITY.x1 + 1, CITY.y0, CITY.x1 + 7, CITY.y1, "grass"); // east apron to the river road

  // 4) Build Ironvale at the centre.
  //    Plaza floor: stone in the civic (west) half, dirt in the market (east).
  carve(CITY.x0, CITY.y0, CITY.x1, CITY.y1, "stone");
  carve(63, CITY.y0 + 1, CITY.x1 - 1, CITY.y1 - 1, "dirt"); // market ground (east)
  //    Wall ring.
  for (let x = CITY.x0; x <= CITY.x1; x++) { set(x, CITY.y0, "wall"); set(x, CITY.y1, "wall"); }
  for (let y = CITY.y0; y <= CITY.y1; y++) { set(CITY.x0, y, "wall"); set(CITY.x1, y, "wall"); }
  //    Main streets (a cross joining the four gates).
  carve(59, CITY.y0, 61, CITY.y1, "path"); // N–S high street
  carve(CITY.x0, 51, CITY.x1, 53, "path"); // E–W high street
  //    Four gates (open the wall ring where the roads meet it).
  carve(GATES.north.x - 1, CITY.y0, GATES.north.x + 1, CITY.y0, "path");
  carve(GATES.south.x - 1, CITY.y1, GATES.south.x + 1, CITY.y1, "path");
  carve(CITY.x0, GATES.west.y - 1, CITY.x0, GATES.west.y + 1, "path");
  carve(CITY.x1, GATES.east.y - 1, CITY.x1, GATES.east.y + 1, "path");
  //    Outlying hamlets get a packed-dirt yard carved first, so the cottages
  //    that follow sit on cleared ground rather than open field (step 4d names
  //    them). Done before the BUILDINGS loop so the footprints stamp over it.
  carve(81, 57, 90, 63, "dirt");  // Redmouth, on the Redrun's east bank
  carve(63, 72, 73, 79, "dirt");  // The Drover's Rest, on the south road
  carve(58, 13, 67, 19, "dirt");  // The Fold, in the upland Knuckle Hills
  //    A vacant homestead lot beside each hamlet — cleared ground a player can
  //    claim and build a home on (the build hotspots sit in these).
  carve(81, 64, 86, 67, "dirt");  // Redmouth homestead lot, south of the bank
  carve(74, 73, 79, 76, "dirt");  // Drover's Rest homestead lot, east of the yard
  carve(59, 8, 64, 11, "dirt");   // The Fold homestead lot, north of the croft
  //    The buildings: each footprint becomes blocking masonry (the renderer
  //    lays a roof over it). The streets and frontages stay clear between them.
  for (const b of BUILDINGS) carve(b.x0, b.y0, b.x1, b.y1, "wall");
  //    A flagged town square around the crossroads, for the fountain + crowds.
  carve(58, 50, 62, 54, "stone");

  // 4b) Countryside features — the bible's named landmarks get real ground, so
  //     the open country between regions reads as places, not blank field.
  // -- The open Knuckle Hills, north-west of the city --
  carve(8, 6, 14, 12, "dirt");     // Rook's Watch — a high firepit knoll
  carve(18, 8, 26, 16, "stone");   // The Knuckle — the bald stone fist
  carve(14, 18, 22, 24, "dirt");   // an ashwood grove
  carve(28, 29, 33, 34, "water");  // a hill tarn (a head-stream pool)
  carve(5, 30, 11, 36, "stone");   // The Coldvein Scar — a played-out cutting
  // -- The river approach & east banks --
  carve(80, 56, 90, 63, "dirt");   // Redrun banks below the east road
  // -- The moor's edge, south of the city --
  carve(40, 73, 49, 82, "bog");    // where the hill country gives out to moor
  // -- The charburner's country, between wood and moor --
  carve(2, 64, 8, 70, "dirt");     // The Charburner's Clearing
  // -- The warm ground spreading south-east (Ashfen apron) --
  carve(72, 86, 86, 101, "ash");   // warm flats reaching toward the river mouth

  // 4c) Heath: give the remaining open grass a rolling, mixed texture instead of
  //     a flat green sheet (all walkable — grass / dirt / moss hill country).
  for (let y = 0; y < OVERWORLD_HEIGHT; y++) {
    for (let x = 0; x < WIDTH; x++) {
      if (tiles[y * WIDTH + x] !== "grass") continue;
      const n = noise(x, y);
      if (n > 0.86) set(x, y, "moss");
      else if (n > 0.66) set(x, y, "dirt");
      // else: stays grass
    }
  }

  // 5) Roads from the gates out to each region (kept 3 wide and walkable).
  //    North gate → the Spine pass (its walkable stone pass is at its west edge).
  carve(59, 34, 61, CITY.y0 - 1, "path");          // up through the grove
  carve(44, 34, 61, 36, "path");                    // west along the foot of the Spine
  carve(44, 13, 46, 36, "stone");                   // up into the Spine pass
  //    West gate → Greyoak Wood (just west).
  carve(34, 51, CITY.x0 - 1, 53, "path");
  //    South gate → Ashfen Flats (south) via a moor road.
  carve(59, CITY.y1 + 1, 61, 82, "path");
  //    South-west spur → Heartmoor.
  carve(36, 68, 61, 70, "path");                    // along the lake's north shore
  carve(34, 70, 36, 80, "path");                    // down the lake's west side to the moor
  //    East gate → the Redrun & the river road.
  carve(CITY.x1 + 1, 51, 96, 53, "path");           // east apron
  carve(94, 53, 96, 62, "dirt");                    // down to the riverbank
  //    North-east spur → the Marrow Deeps cave mouth.
  carve(86, 34, 88, 51, "stone");                   // up the outcrop
  carve(86, 34, 90, 36, "cave");                    // into the cave mouth

  // 5b) Short lanes joining the outlying hamlets to the road network.
  carve(84, 54, 85, 57, "path");                    // Redmouth lane up to the east road
  carve(61, 75, 64, 76, "path");                    // The Drover's Rest, off the south road
  carve(58, 19, 60, 24, "path");                    // The Fold, down to the grove

  // 6) Carve the four sealed boss arenas in the band below the overworld.
  for (const a of ARENAS) {
    for (let y = ARENA_TOP; y < ARENA_TOP + ARENA_H; y++) {
      for (let x = a.x; x < a.x + ARENA_W; x++) {
        const edge = x === a.x || x === a.x + ARENA_W - 1 || y === ARENA_TOP || y === ARENA_TOP + ARENA_H - 1;
        set(x, y, edge ? "wall" : a.floor);
      }
    }
  }

  // 7) Carve each player-home interior in the band below the arenas: a four-room
  //    house (walls shared) with plank floors, then punch the doorways through.
  for (const h of HOMES) {
    const plan = homeLayout(h.ox);
    for (const rm of plan.rooms) {
      for (let y = rm.y0; y <= rm.y1; y++) {
        for (let x = rm.x0; x <= rm.x1; x++) {
          const edge = x === rm.x0 || x === rm.x1 || y === rm.y0 || y === rm.y1;
          set(x, y, edge ? "wall" : "plank");
        }
      }
    }
    for (const d of plan.doorways) set(d.x, d.y, "plank"); // openings (incl. the sealed wing doorway)
  }

  return { name: "Varath", width: WIDTH, height: HEIGHT, tiles };
}

export const map: WorldMap = decode();

/** Convenience: read the tile type at a coordinate (grass if out of bounds). */
export function tileAt(m: WorldMap, x: number, y: number): TileType {
  if (x < 0 || y < 0 || x >= m.width || y >= m.height) return "grass";
  return m.tiles[y * m.width + x]!;
}
